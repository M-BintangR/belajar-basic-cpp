#include <iostream>

using namespace std;

int main()
{
    int a = 10;
    int b = 10;

    // INCREMENT
    cout << "sebelum increment : " << a << endl;
    a++;
    cout << "sesudah increment : " << a << endl;

    // DECEMENT
    cout << "sebelum decerment : " << b << endl;
    b--;
    cout << "sesudah decerment : " << b << endl;

    return 0;
}